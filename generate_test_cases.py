import os
import pandas as pd
from openpyxl import load_workbook

def read_template(template_path):
    """
    读取模板Excel文件，了解其结构
    
    Args:
        template_path: 模板文件路径
    
    Returns:
        tuple: (workbook, sheet_names)
    """
    try:
        wb = load_workbook(template_path)
        sheet_names = wb.sheetnames
        print(f"成功读取模板文件: {template_path}")
        print(f"包含工作表: {sheet_names}")
        return wb, sheet_names
    except Exception as e:
        print(f"读取模板文件失败: {str(e)}")
        return None, []

def analyze_template_structure(template_path):
    """
    分析模板文件的结构，包括列名、数据格式等
    
    Args:
        template_path: 模板文件路径
    """
    try:
        # 使用pandas读取文件
        excel_file = pd.ExcelFile(template_path)
        
        for sheet_name in excel_file.sheet_names:
            print(f"\n=== 分析工作表: {sheet_name} ===")
            
            # 读取数据
            df = pd.read_excel(excel_file, sheet_name=sheet_name)
            
            # 打印基本信息
            print(f"行数: {len(df)}, 列数: {len(df.columns)}")
            print(f"列名: {list(df.columns)}")
            
            # 打印前10行数据
            print("\n前10行数据:")
            print(df.head(10).to_string(index=False))
            
            # 打印数据类型
            print("\n数据类型:")
            print(df.dtypes)
    except Exception as e:
        print(f"分析模板结构失败: {str(e)}")

